package datatypes_assignment;

public class Problem1 {

	public static void main(String[] args) {
		
		float marks = 87.6f;
		System.out.println("Float Value : "+marks);
		double marks_1= marks;
		System.out.println("Double Value : "+marks_1);
		int marks_2 = (int) marks;
		System.out.println("Integer Value : "+marks_2);
		char character = (char) marks;
		System.out.println("Character Value : "+character);
	}

}
